package kr.co.jabusim.beans;

public class EduBean {
	
	private int edu_idx;
	private String user_id;
	private String edu_type;
	private String edu_academy;
	private String edu_major;
	
	
	public int getEdu_idx() {
		return edu_idx;
	}
	public void setEdu_idx(int edu_idx) {
		this.edu_idx = edu_idx;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getEdu_type() {
		return edu_type;
	}
	public void setEdu_type(String edu_type) {
		this.edu_type = edu_type;
	}
	public String getEdu_academy() {
		return edu_academy;
	}
	public void setEdu_academy(String edu_academy) {
		this.edu_academy = edu_academy;
	}
	public String getEdu_major() {
		return edu_major;
	}
	public void setEdu_major(String edu_major) {
		this.edu_major = edu_major;
	}
	
	
	
}
